package co.edu.unbosque.model;

public class PistaMusical {

	private String nombreCancion;
	private String generoMusical;
	private String nombreArtista;
	private String nombreArchivoMusica;

	public PistaMusical() {

	}

	public String getNombreCancion() {
		return nombreCancion;
	}

	public void setNombreCancion(String nombreCancion) {
		this.nombreCancion = nombreCancion;
	}

	public String getGeneroMusical() {
		return generoMusical;
	}

	public void setGeneroMusical(String generoMusical) {
		this.generoMusical = generoMusical;
	}

	public String getNombreArtista() {
		return nombreArtista;
	}

	public void setNombreArtista(String nombreArtista) {
		this.nombreArtista = nombreArtista;
	}

	public String getNombreArchivoMusica() {
		return nombreArchivoMusica;
	}

	public void setNombreArchivoMusica(String nombreArchivoMusica) {
		this.nombreArchivoMusica = nombreArchivoMusica;
	}

}
